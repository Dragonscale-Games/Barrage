#include <Engine/Engine.hpp>

namespace Demo
{
	//! A simple game environment using the engine.
  class Game
  {
    public:
      /**************************************************************/
      /*!
        \brief
          Default constructor.
      */
      /**************************************************************/
      Game();

      /**************************************************************/
      /*!
        \brief
          Runs the game.
      */
      /**************************************************************/
      void Run();

    private:
      /**************************************************************/
      /*!
        \brief
          Initializes Barrage engine and game systems.
      */
      /**************************************************************/
      void Initialize();

      /**************************************************************/
      /*!
        \brief
          Updates main game loop.
      */
      /**************************************************************/
      void Update();

      /**************************************************************/
      /*!
        \brief
          Shuts down Barrage engine and game systems.
      */
      /**************************************************************/
      void Shutdown();

    private:
      Barrage::Engine engine_;  //!< Barrage engine
      bool isRunning_; //!< Keeps track of whether game is running
  };
}

